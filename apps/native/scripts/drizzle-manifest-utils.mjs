import { readFileSync } from "node:fs";
import path from "node:path";

export function getDrizzlePaths(appRoot) {
	const drizzleDir = path.join(appRoot, "drizzle");

	return {
		drizzleDir,
		journalPath: path.join(drizzleDir, "meta", "_journal.json"),
		outputPath: path.join(drizzleDir, "migrations.ts"),
	};
}

export function readJournal(journalPath) {
	return JSON.parse(readFileSync(journalPath, "utf8"));
}

export function buildMigrationEntries(journal) {
	return journal.entries.map((entry, index) => ({
		importName: `m${String(index).padStart(4, "0")}`,
		fileName: `${entry.tag}.sql`,
		tag: entry.tag,
	}));
}

export function buildManifestSource(journal) {
	const migrationEntries = buildMigrationEntries(journal);

	const lines = [
		"// This file is generated by scripts/generate-drizzle-migrations-manifest.mjs",
		"// Do not edit it manually.",
		...migrationEntries.flatMap(({ importName, fileName }) => [
			"// @ts-expect-error - inline-import babel plugin handles .sql imports",
			`import ${importName} from "./${fileName}";`,
		]),
		'import journal from "./meta/_journal.json";',
		"",
		"export default {",
		"\tjournal,",
		"\tmigrations: {",
		...migrationEntries.map(
			({ importName }) => `\t\t${importName}: ${importName},`,
		),
		"\t},",
		"};",
		"",
	];

	return `${lines.join("\n")}`;
}
